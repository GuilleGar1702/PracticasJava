    public void matrizMaker(){
        int n = Integer.parseInt(TFEntrada.getText());
        float [][] matriz = new float [n][n]; 
        for (int i=0; i<n; i++){
            for (int j=0; j<n; j++){
                matriz[i][j] = Integer.parseInt(JOptionPane.showInputDialog("Ingrese valor i: "+j+", j: "+i));
            }
        }
        matrizShower(matriz);
        suMadreXd(matriz, 0);
    }
    public void matrizShower(float [][] matriz){
        int n = matriz.length;
        for (int i=0; i<n; i++){
            for (int j=0; j<n; j++){
                TAMovimientos.append(matriz[i][j] + " ");
            }
            TAMovimientos.append("\n");
        }
        TAMovimientos.append("------------------------\n");
    }
    
    public void suMadreXd(float [][] matriz, int a){
        for (int b=a+1; b<matriz.length;b++){
            if (matriz [a][a] !=0){
                float cociente = -(matriz[b][a] / matriz[a][a]);//formula de la pizarra
                //TAMovimientos.append("----pivot----"+matriz [b][a]+"\n");
                for (int i=a;i<matriz.length;i++){
                    matriz[b][i]+=(cociente*matriz[a][i]);
                }
            }
            matrizShower(matriz);
        }
        a++;
        if (a<matriz.length-1){
            //matrizShower(matriz);
            suMadreXd(matriz, a);
        }
        else{
            //matrizShower(matriz);
            float z =1;
            for (int n=0; n<matriz.length; n++){
                z=z*matriz[n][n];
            }
            TAMovimientos.append("Determinante: "+(float)Math.round(z*1000d)/1000d+"\n");
        }
    }
    
    
