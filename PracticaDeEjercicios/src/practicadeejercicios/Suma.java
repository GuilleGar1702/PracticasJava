    public void suma(int a, int b, int n){
        if (n<1){
            TAMovimientos.append("Solo numeros mayores a 0 (enteros)\n");
        }
        else{
            if (n==1){
                TAMovimientos.append((a-b) +"+"+b+"= "+a+"\n");
            }
            else{
                TAMovimientos.append((a-b) +"+"+b+"= "+a+"\n");
                a=a+b;
                n--;
                suma(a, b, n);
            }
        }
    }
    
    
    
    
